package com.learning.basics;

public class Variables {

    public static void main(String[] args) {
        int number; /* Variable declaration */
        number = 10;
        float n1 = 10.2f;
        char c = '%';
        System.out.println(number);
        System.out.println(n1);
        System.out.println(c);
    }
}

